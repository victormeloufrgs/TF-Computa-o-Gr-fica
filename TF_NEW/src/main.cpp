// Detecta colisão entre objetos (cubos ao redor dos objetos)
bool colidiram(glm::vec4 center1, float y1_size, float x1_size, float z1_size, glm::vec4 center2, float y2_size, float x2_size, float z2_size) {

    glm::vec4 lower_point_obj1 = glm::vec4(center1.x-x1_size/2.0f, center1.y-y1_size/2.0f, center1.z-z1_size/2.0f, center1.w);
    glm::vec4 higher_point_obj1 = glm::vec4(center1.x+x1_size/2.0f, center1.y+y1_size/2.0f, center1.z+z1_size/2.0f, center1.w);

    glm::vec4 lower_point_obj2 = glm::vec4(center2.x-x2_size/2.0f, center2.y-y2_size/2.0f, center2.z-z2_size/2.0f, center2.w);
    glm::vec4 higher_point_obj2 = glm::vec4(center2.x+x2_size/2.0f, center2.y+y2_size/2.0f, center2.z+z2_size/2.0f, center2.w);

    if(isBetween(lower_point_obj1, lower_point_obj2, higher_point_obj2) ||
       isBetween(higher_point_obj1, lower_point_obj2, higher_point_obj2) ||
       isBetween(lower_point_obj2, lower_point_obj1, higher_point_obj1) ||
       isBetween(higher_point_obj2, lower_point_obj1, higher_point_obj1)) {
        return true;
    } else {
        return false;
    }
}

glm::vec4 getMoveVector(glm::vec4 from, glm::vec4 to, float length) {
    glm::vec4 vec_move = to - from;
    vec_move = (length/norm(vec_move))*vec_move;
    return vec_move;
}

bool isBetween(glm::vec4 point, glm::vec4 min_point, glm::vec4 max_point) {

    if (point.x >= min_point.x && point.y >= min_point.y && point.z >= min_point.z) {
        if (point.x <= max_point.x && point.y <= max_point.y && point.z <= max_point.z) {
            return true;
        }
    }
    return false;
}

glm::vec4 getPositionAnimationBezierCubic(glm::vec4 cp1, glm::vec4 cp2, glm::vec4 cp3, glm::vec4 cp4, float timeInSec) {


    float t = timeInSec;

    float c12x = cp1.x + t*(cp2.x - cp1.x);
    float c23x = cp2.x + t*(cp3.x - cp2.x);
    float c34x = cp3.x + t*(cp4.x - cp3.x);

    float c123x = c12x + t*(c23x - c12x);
    float c234x = c23x + t*(c34x - c23x);

    float cx = c123x + t*(c234x - c123x);

    // -----

    float c12y = cp1.y + t*(cp2.y - cp1.y);
    float c23y = cp2.y + t*(cp3.y - cp2.y);
    float c34y = cp3.y + t*(cp4.y - cp3.y);

    float c123y = c12y + t*(c23y - c12y);
    float c234y = c23y + t*(c34y - c23y);

    float cy = c123y + t*(c234y - c123y);

    // ----

    float c12z = cp1.z + t*(cp2.z - cp1.z);
    float c23z = cp2.z + t*(cp3.z - cp2.z);
    float c34z = cp3.z + t*(cp4.z - cp3.z);

    float c123z = c12z + t*(c23z - c12z);
    float c234z = c23z + t*(c34z - c23z);

    float cz = c123z + t*(c234z - c123z);


//      std::cout << "timeInSec" << timeInSec << std::endl;
//    std::cout << "xPos" << xPos << std::endl;
//    std::cout << "yPos" << yPos << std::endl;
//    std::cout << "zPos" << zPos << std::endl;
    return glm::vec4(cx, cy, cz, 1.0f);

}

