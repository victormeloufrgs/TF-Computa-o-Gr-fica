    if((last_camera_view_vector_temp.y < -0.99f && mouse_dy > 0.0f) || (last_camera_view_vector_temp.y > 0.99f && mouse_dy < 0.0f)) {
        camera_view_vector_temp = Matrix_Rotate(0.0f*ROTATION_SPEED,toRotateAround)*camera_view_vector;
        g_CameraPhi   -= 0.01f*mouse_dy;
    }

    last_camera_view_vector_temp = camera_view_vector_temp;
